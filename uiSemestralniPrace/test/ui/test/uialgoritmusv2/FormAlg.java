    private void naplNasledujiciStavy(Hra stav) {
        jPanelNaslStavy.removeAll();
        GridBagConstraints gridBagConstraints = new java.awt.GridBagConstraints();
        gridBagConstraints.insets = new Insets(5, 5, 5, 5);
        gridBagConstraints.gridy = 0;
        gridBagConstraints.gridheight = 2;
        gridBagConstraints.anchor = java.awt.GridBagConstraints.WEST;
        Iterator<Stav<Integer[]>> iterator = stav.getNasledujiStavy().iterator();
        int i = 0;
        while (iterator.hasNext()) {
            PanelHra panelHra = new PanelHra();
            panelHra.setData(iterator.next().getValue());
            gridBagConstraints.gridx = i;
            i++;
            if (i == stav.getNasledujiStavy().size()) {
                gridBagConstraints.weightx = 1.0;
            }
            jPanelNaslStavy.add(panelHra, gridBagConstraints);
        }
    }

