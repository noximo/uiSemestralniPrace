    private void jButtonVygeberujActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVygeberujActionPerformed
        // TODO add your handling code here:
        ArrayList<Integer> pokus = new ArrayList<Integer>();
        for (int i = 0; i < 10; i++) {
            pokus.add(pocatecni.getValue()[i]);
        }
        Collections.shuffle(pokus);
        Integer[] vysl = new Integer[10];
//        for (int i = 0; i < 10; i++) {
//            vysl[i] =  pokus.get(i);
//        }
        for (int j = 0, i = 0; i < 10; i++) {
            if (pokus.get(i) != 9) {
                vysl[j] = pokus.get(i);
                j++;
            }
        }
        vysl[9] = 9;

        pocatecni.setValue(vysl);
        panelHraPocatecni.setData(pocatecni.getValue());
        naplNasledujiciStavy(pocatecni);
    }//GEN-LAST:event_jButtonVygeberujActionPerformed

    private void jButtonVypocitejActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVypocitejActionPerformed
        // TODO add your handling code here:
        Integer[] pole = new Integer[10];
        for (int i = 0; i < 9; i++) {
            pole[i] = i + 1;
        }
        pole[9] = 0;
        Hra koncovy = new Hra(pole, 0, null);
        Algortimus alg = new Algortimus(pocatecni, koncovy);
        koncovyStav = alg.spust();
        if (koncovyStav == null) {
            JOptionPane.showMessageDialog(this, "Nemá řešení");
        } else {
            panelHraKonec.setData((Integer[]) koncovyStav.getKonecnyStav().getValue());
            jTextFieldAlgKroky.setText(Integer.toString(koncovyStav.getPocetKrokuCelkem()));
            jTextFieldKroky.setText(Integer.toString(koncovyStav.getPocetKroku()));
            jTextFieldAktKroku.setText(Integer.toString(koncovyStav.getPocetKroku()));
            panelHraKroky.setData((Integer[]) koncovyStav.getPosloupnostKroku().get(koncovyStav.getPosloupnostKroku().size() - 1).getValue());
            aktKrok = 0;
            jTextFieldPocKroku.setText("0");
            jButtonDalsi.setEnabled(true);
        }
    }//GEN-LAST:event_jButtonVypocitejActionPerformed

    private void jButtonDalsiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDalsiActionPerformed
        // TODO add your handling code here:
        aktKrok++;
        panelHraKroky.setData((Integer[]) koncovyStav.getPosloupnostKroku().get(
                koncovyStav.getPosloupnostKroku().size() - aktKrok - 1).getValue());
        jTextFieldPocKroku.setText(Integer.toString(aktKrok));
        if(aktKrok == koncovyStav.getPocetKroku()){
             jButtonDalsi.setEnabled(false);
        }
    }//GEN-LAST:event_jButtonDalsiActionPerformed

